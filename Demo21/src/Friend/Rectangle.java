package Friend;

public class Rectangle {

	public static void main(String[] args) {
		int a=5;//宽=5
		int b=7;//长=7
		int c=(a+b)*2;//周长
		int d=a*b;//面积
		System.out.println("长方形的宽为："+a);
		System.out.println("长方形的长为："+b);
		System.out.println("长方形的周长为："+c);
		System.out.println("长方形的面积为："+d);
	}

}
